export default async function handler(req, res) {
  if (req.method !== "POST") return res.status(405).end();

  const { messages, hat = "lawyer", rights = [] } = req.body;

  // בנה context מהזכויות
  const rightsCtx = rights
    .map(r => `• [${r.category}] ${r.title}: ${r.details}${r.tip ? ` (טיפ: ${r.tip})` : ""}`)
    .join("\n");

  const hatPrompt = HAT_PROMPTS[hat] || HAT_PROMPTS.lawyer;

  const system = `${hatPrompt}

---
בסיס הידע שלך — זכויות פצועי צה"ל:
${rightsCtx}

---
הנחיות כלליות:
- דבר בעברית פשוטה, ישירה, אנושית
- שאל שאלות כדי להתאים — לא להרצות
- היה אקטיבי: הצע, שאל, אל תחכה שישאלו אותך
- קו חם: מוקד פצועים *6500 | נפש אחת *8944 | אגף השיקום mod.gov.il
- אורך תשובה: 3-6 שורות — לא יותר, לא פחות. שמור על קצב שיחה`;

  try {
    const r = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: {
        "x-api-key": process.env.ANTHROPIC_API_KEY,
        "anthropic-version": "2023-06-01",
        "content-type": "application/json",
      },
      body: JSON.stringify({
        model: "claude-sonnet-4-6",
        max_tokens: 600,
        system,
        messages: messages.map(m => ({ role: m.role, content: m.content })),
      }),
    });

    if (!r.ok) {
      const err = await r.text();
      console.error("Claude API error:", err);
      return res.status(500).json({ reply: "שגיאה בחיבור ל-Claude." });
    }

    const d = await r.json();
    const reply = d.content?.[0]?.text || "לא הצלחתי לענות, נסה שוב.";
    res.json({ reply });
  } catch (err) {
    console.error("API route error:", err);
    res.status(500).json({ reply: "שגיאה פנימית." });
  }
}
